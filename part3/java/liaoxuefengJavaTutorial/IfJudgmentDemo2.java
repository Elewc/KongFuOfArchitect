public class IfJudgmentDemo2 {
    public static void main(String[] args) {
//        if语句还可以编写一个else { ... }，当条件判断为false时，将执行else的语句块：
        int n = 70;
        if (n >= 60) {
            System.out.println("及格了");
        } else {
            System.out.println("挂科了");
        }
        System.out.println("END");

//        注意，else不是必须的。
//
//还可以用多个if ... else if ...串联。例如：
    }
}
